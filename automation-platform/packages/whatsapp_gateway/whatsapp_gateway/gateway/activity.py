def activity(
    session: Session,
    account: WhatsAppAccount | None,
    event_type: str,
    message: str,
    *,
    level: str = "info",
    details: dict[str, Any] | None = None,
) -> None:
    session.add(
        WhatsAppActivity(
            account_id=account.id if account else None,
            event_type=event_type,
            level=level,
            message=message,
            details=details,
        )
    )
